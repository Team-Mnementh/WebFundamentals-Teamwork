		<p>
			О, Windows!<br />
			Три дена храбро инсталирах.<br />
			Трих, слагах, регистрирах.<br />
			Не тръгваш и не тръгваш сам,<br />
			показваш само син екран.<br />
			Първо ми съсипваш FAT,<br />
			преминавам през format,<br />
			но не стига таз беля,<br />
			нов дял трябва да ти заделя.<br />
		</p>
		<p>
			Хоп, пак не се получи<br />
			работата се закучи,<br />
			драйвер някакъв незнаен,<br />
			искаш в цикъл най-безкраен.<br />
		</p>
		<p>
			С Wizardа отново споря,<br />
			но не мога да го сборя.<br />
			Нямам Plug And Play модем,<br />
			разбери го бе, кретен!<br />
		</p>
		<p>
			Изнервям се, та чак треперя.<br />
			CD-то искам да намеря,<br />
			та да тръгнеш най-подир,<br />
			а не под DOS да пиша Dir.<br />
		</p>
		<p>
			Уж тръгна, пуснах Service Pack,<br />
			О, не - ще инстарирам пак!<br />
			Най-после посред нощ,<br />
			използвах софтуера лош.<br />
		</p>
		<p>
			На сутринта от шлаката изрових,<br />
			процесор спечен и пророних:<br />
			Да бе мирно си седяло,<br />
			не би от Setup умряло. <br />
		</p>
	</article>

